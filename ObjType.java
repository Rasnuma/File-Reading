
public class ObjType {
    public float first;
    public float second;
    public float third;
    public float forth;
    public String fifth;

    public ObjType(float first, float second, float third, float forth, String fifth) {
        this.first = first;
        this.second = second;
        this.third = third;
        this.forth = forth;
        this.fifth = fifth;
    }

    public float getFirst() {
        return first;
    }

    public void setFirst(float first) {
        this.first = first;
    }

    public float getSecond() {
        return second;
    }

    public void setSecond(float second) {
        this.second = second;
    }

    public float getThird() {
        return third;
    }

    public void setThird(float third) {
        this.third = third;
    }

    public float getForth() {
        return forth;
    }

    public void setForth(float forth) {
        this.forth = forth;
    }

    public String getFifthStr() {
        return fifth;
    }

    public void setFifthStr(String fifth) {
        this.fifth = fifth;
    }
}
